export const litexCatalogBoards: BoardDefinition[] = LITEX_CATALOG_SPECS.map(makeCatalogBoard);

function makeCatalogGroup({
  id,
  name,
  vendor,
  device,
  boardIds,
}: {
  id: string;
  name: string;
  vendor: string;
  device: string;
  boardIds: string[];
}) {
  return {
    id,
    name,
    vendor,
    device,
    variants: boardIds
      .map((id) => litexCatalogBoards.find((board) => board.id === id))
      .filter((board): board is BoardDefinition => Boolean(board))
      .map((board) => ({
        id: board.id,
        name: board.name,
        fpga: board.device,
      })),
  };
}

function getCatalogBoard(id: string): BoardDefinition {
  const board = litexCatalogBoards.find((catalogBoard) => catalogBoard.id === id);

  if (!board) {
    throw new Error(`Missing LiteX catalog board: ${id}`);
  }

  return board;
}

