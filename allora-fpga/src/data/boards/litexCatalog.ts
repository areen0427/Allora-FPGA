import type { BoardDefinition, BoardPin, ConstraintFile, SynthesisFlow } from "../boardTypes";

type PinSpec = [string, string, string, BoardPin["type"], string, boolean?];

type CatalogBoardSpec = {
  id: string;
  name: string;
  vendor: string;
  family: string;
  device: string;
  packageName: string;
  fpgaId: string;
  constraintsFile: ConstraintFile;
  synthesisFlow: SynthesisFlow;
  toolchain: string;
  program: string;
  clock: { name: string; pin: string; frequency: number };
  pins: PinSpec[];
  source: string;
};

function toPin([name, signal, pin, type, group, activeLow]: PinSpec): BoardPin {
  return { name, signal, pin, type, group, activeLow, verified: true };
}

function makeCatalogBoard(spec: CatalogBoardSpec): BoardDefinition {
  const pins = spec.pins.map(toPin);
  return {
    id: spec.id,
    name: spec.name,
    vendor: spec.vendor,
    family: spec.family,
    device: spec.device,
    package: spec.packageName,
    fpgaId: spec.fpgaId,
    constraintsFile: spec.constraintsFile,
    synthesisFlow: spec.synthesisFlow,
    toolchain: { synth: spec.toolchain, placeRoute: spec.toolchain, program: spec.program },
    clocks: [{ ...spec.clock, verified: spec.clock.pin !== "unknown" }],
    pins,
    leds: pins.filter((pin) => pin.type === "led"),
    buttons: pins.filter((pin) => pin.type === "button"),
    notes: `Pin mapping imported from the LiteX ${spec.source} platform. Large memory, SERDES, and connector buses are summarized for the app catalog.`,
  };
}

